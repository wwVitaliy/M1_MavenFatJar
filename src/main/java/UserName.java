public class UserName {
    String name;
    String lastName;

    public UserName(String name, String sNane) {
        this.name = name;
        this.lastName = sNane;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
}
